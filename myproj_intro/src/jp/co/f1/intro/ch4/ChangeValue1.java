package jp.co.f1.intro.ch4;

public class ChangeValue1 {

	public static void main(String[] args) {

		// 世界一の塔の高さを管理する変数
		int maxHeight = 600;

		System.out.print("世界一高い自立型鉄塔の高さは、");
		System.out.print(maxHeight);
		System.out.println("メートルでした。");

		maxHeight = 634;

		System.out.print("東京スカイツリーが完成し、");
		System.out.print(maxHeight);
		System.out.println("メートルになりました。");

	}

}
