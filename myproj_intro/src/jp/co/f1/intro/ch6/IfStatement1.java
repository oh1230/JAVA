package jp.co.f1.intro.ch6;

public class IfStatement1 {

	public static void main(String[] args) {
		// 比較用の値を管理する変数の宣言と初期化
		int a = 2;
		int b = 2;

		System.out.println("-- if 文開始 --");

		if (a == b) {
			System.out.println("変数 a と変数 b は等しい。");
		}

		System.out.println("-- if 文終了 --");

	}

}
