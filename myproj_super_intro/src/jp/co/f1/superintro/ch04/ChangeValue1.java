package jp.co.f1.superintro.ch04;

public class ChangeValue1 {

	public static void main(String[] args) {
		
		//年
		int year = 1964;
		
		System.out.print("東京オリンピックは");
		System.out.print(year);
		System.out.println("年に、開催が決定しました！");
		
		//変数に別の値を代入
		year = 2020;
		
		System.out.print("2回目は");
		System.out.print(year);
		System.out.println("年に、開催が決定しました！");

	}

}
